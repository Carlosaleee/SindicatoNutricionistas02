    private void botaoCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularActionPerformed
         
        try {
    double peso = Double.parseDouble(jTextField1.getText());
    double altura = Double.parseDouble(jTextField2.getText());
    double imc = peso / (altura * altura);

    DecimalFormat df = new DecimalFormat("#.00000000");
    
    jLabel4.setText("Resultado: " + df.format(imc) );

    if (imc < 18.5) {
        jLabel5.setText("Classificação: Abaixo do Peso");
    } 
    else if (imc >= 18.5 && imc < 25) {
        jLabel5.setText("Classificação: Peso Normal");
    } 
    else if (imc >= 25 && imc < 30) {
        jLabel5.setText("Classificação: Sobrepeso");
    } 
    else {
        jLabel5.setText("Classificação: Obesidade");
    }
    
    
     jTextField1.setText("");
     jTextField2.setText("");
     
} catch (NumberFormatException e) {
    
    JOptionPane.showMessageDialog(null,"Por favor, insira valores válidos para Peso e Altura.");
}      
    }//GEN-LAST:event_botaoCalcularActionPerformed

    
