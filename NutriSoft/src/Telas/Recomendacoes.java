    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try {
        double calorias = Double.parseDouble(jTextField1.getText());
        
        double carboidratos = (calorias * 0.5) / 4;
        double proteinas = (calorias * 0.25) / 4;
        double gordura = (calorias * 0.25) / 9;
        
        DecimalFormat df = new DecimalFormat("#.00");
        
        jLabel3.setText("Carboidratos : " + df.format(carboidratos) + " g");
        jLabel4.setText("Proteínas : " + df.format(proteinas) + " g");
        jLabel5.setText("Gordura : " + df.format(gordura) + " g");
        
        jTextField1.setText("");
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, preencha a quantidade de calorias com um valor válido.");
    }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
